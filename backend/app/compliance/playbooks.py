import uuid
from datetime import datetime
from typing import Any

import structlog
import yaml
from pydantic import BaseModel, ConfigDict, Field

from app.compliance.labeling import (
    AlgorithmFinding,
    ComplianceLabelResult,
    QuantumLabel,
)

logger = structlog.get_logger(__name__)


# ─────────────────────────────────────────────────────────────────────────────
# Enums & Schemas
# ─────────────────────────────────────────────────────────────────────────────

class PlaybookTier(str, enum.Enum):
    CRITICAL = "critical"
    HIGH     = "high"
    MEDIUM   = "medium"
    LOW      = "low"


class ApprovalStatus(str, enum.Enum):
    PENDING   = "pending"
    APPROVED  = "approved"
    REJECTED  = "rejected"
    NO_GATE   = "no_gate"     # Non-critical playbooks; no approval required


class AnsibleTask(BaseModel):
    """A single Ansible task dict (conforms to Ansible task YAML schema)."""
    name:        str
    module_data: dict[str, Any]   # The Ansible module name is a key
    tags:        list[str] = Field(default_factory=list)
    notify:      list[str] | None = None
    when:        str | None = None

    def to_ansible_dict(self) -> dict[str, Any]:
        """Serialise to a valid Ansible task dict."""
        task: dict[str, Any] = {"name": self.name}
        task.update(self.module_data)
        if self.tags:
            task["tags"] = self.tags
        if self.notify:
            task["notify"] = self.notify
        if self.when:
            task["when"] = self.when
        return task


class RemediationPlaybook(BaseModel):
    """A complete Ansible-compatible playbook for one asset."""
    model_config = ConfigDict(from_attributes=True)

    playbook_id:     str = Field(default_factory=lambda: str(uuid.uuid4()))
    asset_id:        uuid.UUID | None = None
    host:            str | None = None
    tier:            PlaybookTier
    title:           str
    description:     str
    algorithms_targeted: list[str]
    ansible_tasks:   list[AnsibleTask]
    approval_status: ApprovalStatus = ApprovalStatus.NO_GATE
    approval_ticket: str | None = None   # ServiceNow/Jira ticket ID
    generated_at:    datetime = Field(default_factory=datetime.utcnow)
    estimated_effort_hours: float = 0.0

    def to_ansible_yaml(self) -> str:
        """Export the playbook as a complete Ansible YAML playbook string."""
        play = {
            "name": self.title,
            "hosts": self.host or "target_host",
            "become": True,
            "vars": {
                "playbook_id": self.playbook_id,
                "asset_id": str(self.asset_id) if self.asset_id else "unknown",
                "generated_at": self.generated_at.isoformat(),
            },
            "tasks": [task.to_ansible_dict() for task in self.ansible_tasks],
        }
        header = textwrap.dedent(f"""\
            # ═══════════════════════════════════════════════════════════════════
            # PQC Remediation Playbook — {self.tier.value.upper()}
            # Generated by Quantum-Proof Systems Scanner
            # Playbook ID : {self.playbook_id}
            # Asset       : {self.host or self.asset_id}
            # Approval    : {self.approval_status.value}
            # Ticket      : {self.approval_ticket or 'N/A'}
            # ═══════════════════════════════════════════════════════════════════
            """)
        return header + yaml.dump([play], default_flow_style=False, sort_keys=False)


