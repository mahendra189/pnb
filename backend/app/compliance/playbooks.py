"""
compliance/playbooks.py — Actionable Remediation Playbook Generator.

Generates deterministic, context-specific remediation playbooks as
Ansible-compatible YAML tasks for each quantum-vulnerable asset.

Playbook tiers:
  • CRITICAL  — Immediate emergency order: HSM re-keying, emergency ACME cert rotation
  • HIGH      — Planned migration: TLS config hardening, library upgrade
  • MEDIUM    — Scheduled: configuration audit, hybrid KEM enablement
  • LOW       — Informational: monitoring, future-proofing notes

Human-in-the-Loop gate:
  Critical-tier playbooks MUST obtain approval via ServiceNow or Jira ticket
  before execution. The gate stub calls the external REST API and blocks
  until the ticket transitions to "Approved" state.
"""

from __future__ import annotations

import enum
