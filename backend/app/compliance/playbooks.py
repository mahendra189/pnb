def _task_update_openssl(host: str, target_algo: str) -> AnsibleTask:
    return AnsibleTask(
        name=f"Update OpenSSL to support PQC hybrid KEM on {host}",
        module_data={
            "ansible.builtin.package": {
                "name": ["openssl", "liboqs", "oqs-provider"],
                "state": "latest",
            }
        },
        tags=["pqc", "openssl", "phase1"],
        notify=["Restart nginx", "Restart apache2"],
    )


def _task_configure_tls_policy(cipher_policy: str = "PQC_HYBRID") -> AnsibleTask:
    return AnsibleTask(
        name=f"Apply TLS crypto policy: {cipher_policy}",
        module_data={
            "ansible.builtin.command": {
                "cmd": f"update-crypto-policies --set {cipher_policy}",
                "creates": f"/etc/crypto-policies/state/current",
            }
        },
        tags=["tls", "crypto-policy", "phase1"],
    )


def _task_nginx_tls_hardening(host: str) -> AnsibleTask:
    return AnsibleTask(
        name=f"Harden nginx TLS configuration on {host}",
        module_data={
            "ansible.builtin.lineinfile": {
                "path": "/etc/nginx/nginx.conf",
                "regexp": "^(\\s*ssl_protocols)",
                "line": "    ssl_protocols TLSv1.3;  # TLS 1.2 disabled per PQC policy",
                "backrefs": True,
            }
        },
        tags=["nginx", "tls", "hardening"],
        notify=["Reload nginx"],
    )


def _task_disable_deprecated_ciphers() -> AnsibleTask:
    return AnsibleTask(
        name="Disable deprecated cipher suites (RC4, 3DES, SHA-1, export-grade)",
        module_data={
            "ansible.builtin.template": {
                "src":  "templates/openssl_no_deprecated.cnf.j2",
                "dest": "/etc/ssl/openssl.cnf",
                "mode": "0644",
            }
        },
        tags=["ciphers", "hardening", "critical"],
        notify=["Restart sshd", "Restart stunnel"],
    )


def _task_rotate_certificate(host: str) -> AnsibleTask:
    return AnsibleTask(
        name=f"Rotate TLS certificate to ML-DSA-65 on {host}",
        module_data={
            "community.crypto.acme_certificate": {
                "account_email":    "pki-ops@pnb.co.in",
                "csr":              f"/etc/ssl/certs/{host}.csr",
                "dest":             f"/etc/ssl/certs/{host}.crt",
                "acme_directory":   "https://acme-v02.api.letsencrypt.org/directory",
                "acme_version":     2,
                "challenge":        "dns-01",
                "remaining_days":   30,
            }
        },
        tags=["certificate", "pki", "rotation"],
    )


def _task_enable_hybrid_kem(host: str) -> AnsibleTask:
    return AnsibleTask(
        name=f"Enable X25519Kyber768 hybrid KEM on {host}",
        module_data={
            "ansible.builtin.blockinfile": {
                "path":   "/etc/nginx/conf.d/tls.conf",
                "block":  (
                    "ssl_conf_command Ciphersuites "
                    "TLS_AES_256_GCM_SHA384:TLS_CHACHA20_POLY1305_SHA256;\n"
                    "ssl_conf_command Groups x25519kyber768:x25519:secp256r1;"
                ),
                "marker": "# {mark} PQSS HYBRID KEM BLOCK",
            }
        },
        tags=["kem", "hybrid", "tls13"],
        notify=["Reload nginx"],
    )


def _task_hsm_rekey(host: str, algorithm: str) -> AnsibleTask:
    return AnsibleTask(
        name=f"Trigger HSM re-keying for {algorithm} on {host} (CRITICAL)",
        module_data={
            "ansible.builtin.uri": {
                "url":        "https://{{ hsm_api_endpoint }}/v1/rekey",
                "method":     "POST",
                "body_format": "json",
                "body": {
                    "host":      host,
                    "algorithm": "ML-KEM-1024",
                    "key_label": f"pqc_{host.replace('.', '_')}_2026",
                    "reason":    f"PQC migration from {algorithm}",
                },
                "headers": {"Authorization": "Bearer {{ hsm_api_token }}"},
                "status_code": [200, 201],
            }
        },
        tags=["hsm", "critical", "rekey"],
        when="approval_status == 'approved'",
    )


def _task_verify_pqc_handshake(host: str) -> AnsibleTask:
    return AnsibleTask(
        name=f"Verify PQC handshake on {host} using sslyze",
        module_data={
            "ansible.builtin.command": {
                "cmd": f"sslyze --json_out=/tmp/sslyze_{host}.json {host}:443"
            }
        },
        tags=["verify", "sslyze", "post-deploy"],
    )


def _task_send_notification(playbook_id: str, tier: str) -> AnsibleTask:
    return AnsibleTask(
        name="Send Slack/email notification on completion",
        module_data={
            "community.general.slack": {
                "token":    "{{ slack_bot_token }}",
                "msg":      f"✅ PQC Playbook *{playbook_id}* [{tier.upper()}] completed on `{{{{ inventory_hostname }}}}`.",
                "channel":  "#pqc-alerts",
                "icon_emoji": ":lock:",
            }
        },
        tags=["notification", "slack"],
    )


