# Playbook Generator
# ─────────────────────────────────────────────────────────────────────────────

class RemediationPlaybookGenerator:
    """
    Generates Ansible-compatible YAML remediation playbooks from compliance labels.

    Usage::

        gen = RemediationPlaybookGenerator()
        playbooks = gen.generate(compliance_result)
        for pb in playbooks:
            print(pb.to_ansible_yaml())
    """

    def _determine_tier(self, result: ComplianceLabelResult) -> PlaybookTier:
        """Map compliance label + deprecated count to playbook tier."""
        if result.deprecated_count > 0:
            return PlaybookTier.CRITICAL
        if result.quantum_label == QuantumLabel.QUANTUM_VULNERABLE:
            return PlaybookTier.HIGH
        if result.quantum_label == QuantumLabel.PQC_READY:
            return PlaybookTier.MEDIUM
        return PlaybookTier.LOW

    def _build_tasks(
        self,
        host: str,
        tier: PlaybookTier,
        findings: list[AlgorithmFinding],
    ) -> tuple[list[AnsibleTask], float]:
        """Build task list and effort estimate for the given tier."""
        tasks: list[AnsibleTask] = []
        effort = 0.0

        deprecated_algos = [f.algorithm for f in findings if f.is_deprecated]
        vulnerable_algos = [f.algorithm for f in findings if not f.is_approved and not f.is_hybrid]

        if tier == PlaybookTier.CRITICAL:
            tasks.append(_task_disable_deprecated_ciphers())
            for algo in deprecated_algos:
                tasks.append(_task_hsm_rekey(host, algo))
            tasks.append(_task_rotate_certificate(host))
            tasks.append(_task_update_openssl(host, "ml-kem-1024"))
            tasks.append(_task_configure_tls_policy("PQC_HYBRID"))
            tasks.append(_task_nginx_tls_hardening(host))
            tasks.append(_task_enable_hybrid_kem(host))
            tasks.append(_task_verify_pqc_handshake(host))
            effort = 16.0

        elif tier == PlaybookTier.HIGH:
            tasks.append(_task_update_openssl(host, "ml-kem-768"))
            tasks.append(_task_configure_tls_policy("PQC_HYBRID"))
            tasks.append(_task_nginx_tls_hardening(host))
            tasks.append(_task_enable_hybrid_kem(host))
            tasks.append(_task_rotate_certificate(host))
            tasks.append(_task_verify_pqc_handshake(host))
            effort = 8.0

        elif tier == PlaybookTier.MEDIUM:
            tasks.append(_task_enable_hybrid_kem(host))
            tasks.append(_task_configure_tls_policy("PQC_HYBRID"))
            tasks.append(_task_verify_pqc_handshake(host))
            effort = 4.0

        else:  # LOW
            tasks.append(_task_verify_pqc_handshake(host))
            effort = 1.0

        tasks.append(_task_send_notification("{{ playbook_id }}", tier.value))
        return tasks, effort

    def generate(self, result: ComplianceLabelResult) -> list[RemediationPlaybook]:
        """
        Generate one or more Ansible playbooks for a compliance label result.

        Critical-tier playbooks get `approval_status=PENDING` so the approval
        gate can be invoked before execution.

        Returns a list (may be empty if label is FULLY_QUANTUM_SAFE with no deprecated).
        """
        host = result.host or str(result.asset_id or "unknown")
        tier = self._determine_tier(result)

        if tier == PlaybookTier.LOW and result.quantum_label == QuantumLabel.FULLY_QUANTUM_SAFE:
            logger.info("no_playbook_needed", host=host, label=result.quantum_label)
            return []

        tasks, effort = self._build_tasks(host, tier, result.findings)
        algos_targeted = [
            f.algorithm for f in result.findings
            if not f.is_approved or f.is_deprecated
        ]

        approval_status = (
            ApprovalStatus.PENDING if tier == PlaybookTier.CRITICAL else ApprovalStatus.NO_GATE
        )

        playbook = RemediationPlaybook(
            asset_id=result.asset_id,
            host=host,
            tier=tier,
            title=f"PQC Migration — {tier.value.upper()}: {host}",
            description=(
                f"Remediate quantum-vulnerable cryptography on {host}. "
                f"Label: {result.quantum_label.value}. "
                f"Targets: {', '.join(algos_targeted) or 'none'}."
            ),
            algorithms_targeted=algos_targeted,
            ansible_tasks=tasks,
            approval_status=approval_status,
            estimated_effort_hours=effort,
        )

        logger.info(
            "playbook_generated",
            host=host,
            tier=tier.value,
            tasks=len(tasks),
            effort_hours=effort,
            approval=approval_status.value,
        )
        return [playbook]
